import { defineSchema, defineTable } from "convex/server";
import { v } from "convex/values";

export default defineSchema({
  usuarios: defineTable({
    nome: v.string(),
    email: v.string(),
    senhaHash: v.string(),
    criadoEm: v.string(),
  }).index("by_email", ["email"]),
  sessoes: defineTable({
    token: v.string(),
    usuarioId: v.id("usuarios"),
    criadoEm: v.string(),
  }).index("by_token", ["token"]),
  tarefas: defineTable({
    texto: v.string(),
    usuarioId: v.id("usuarios"),
    usuarioNome: v.string(),
    criadaEm: v.string(),
  }).index("by_usuario", ["usuarioId"]),
  pdfs: defineTable({
    nome: v.string(),
    searchNome: v.string(),
    arquivoNome: v.string(),
    contentType: v.optional(v.string()),
    storageId: v.id("_storage"),
    usuarioId: v.id("usuarios"),
    criadoEm: v.string(),
  }).index("by_usuario", ["usuarioId"]),
  maquinas: defineTable({
    apelido: v.string(),
    nomeMaquina: v.string(),
    valorHoraMaquina: v.number(),
    usuarioId: v.id("usuarios"),
    criadoEm: v.string(),
    atualizadoEm: v.string(),
  }).index("by_usuario", ["usuarioId"]),
  operacoes: defineTable({
    nomeOperacao: v.string(),
    usuarioId: v.id("usuarios"),
    criadoEm: v.string(),
    atualizadoEm: v.string(),
  }).index("by_usuario", ["usuarioId"]),
  materiais: defineTable({
    codigo: v.string(),
    descricao: v.string(),
    precoKg: v.number(),
    usuarioId: v.id("usuarios"),
    criadoEm: v.string(),
    atualizadoEm: v.string(),
  }).index("by_usuario", ["usuarioId"]),
  vchhmRows: defineTable({
    sku: v.string(),
    skuKey: v.string(),
    nomeSku: v.string(),
    maquina: v.string(),
    operacao: v.string(),
    descricao: v.string(),
    tempoPreparacao: v.number(),
    tempoPorPeca: v.number(),
    eficiencia: v.number(),
    valorPreparacao: v.number(),
    valorPreparacaoHora: v.number(),
    materialBarra: v.string(),
    descricaoMaterial: v.string(),
    tamanhoPorPecaMaterial: v.string(),
    descricaoItem: v.string(),
    programas: v.string(),
    ferramentas: v.string(),
    usuarioId: v.id("usuarios"),
    criadoEm: v.string(),
    atualizadoEm: v.string(),
  })
    .index("by_usuario", ["usuarioId"])
    .index("by_usuario_sku", ["usuarioId", "skuKey"]),
  productionSheets: defineTable({
    sku: v.string(),
    skuKey: v.string(),
    nomeSku: v.string(),
    nomeCliente: v.string(),
    quantidadeProduzir: v.number(),
    numeroPedido: v.string(),
    pedido: v.string(),
    dataPrevista: v.string(),
    numeroOp: v.string(),
    observacao: v.optional(v.string()),
    custoPreparacao: v.number(),
    valorProduto: v.number(),
    vchhmTotal: v.number(),
    usuarioId: v.id("usuarios"),
    criadoEm: v.string(),
    atualizadoEm: v.string(),
  })
    .index("by_usuario", ["usuarioId"])
    .index("by_usuario_sku", ["usuarioId", "skuKey"]),
  anotacoes: defineTable({
    nome: v.string(),
    descricao: v.string(),
    usuarioId: v.id("usuarios"),
    criadoEm: v.string(),
    atualizadoEm: v.string(),
  }).index("by_usuario", ["usuarioId"]),
}, { schemaValidation: false });
